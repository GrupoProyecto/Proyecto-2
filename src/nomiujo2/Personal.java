    private void jcbPersonalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbPersonalActionPerformed
        if(jcbPersonal.getSelectedItem().equals("Seleccione...")){
        ttablaPlanta.setVisible(false);
        ttablaDocente.setVisible(false);
        lblPersonal.setText("");
        jsp1.setVisible(false);
        jsp2.setVisible(false);
        }
        else if(jcbPersonal.getSelectedItem().equals("Personal de Planta")){
            ttablaPlanta.setVisible(true);
            ttablaDocente.setVisible(false);
            lblPersonal.setText("Datos del Personal de Planta");
            jsp1.setVisible(false);
            jsp2.setVisible(true);
        }
        else if(jcbPersonal.getSelectedItem().equals("Personal Docente")){
            ttablaDocente.setVisible(true);
            ttablaPlanta.setVisible(false);
            jsp1.setVisible(true);
            jsp2.setVisible(false);
            lblPersonal.setText("Datos del Personal Docente");
        }
    }//GEN-LAST:event_jcbPersonalActionPerformed

